import java.util.ArrayList;
import java.util.List;

class Solution {
    private ArrayList<Integer> res = new ArrayList<>();
    public List<Integer> preorder(Node root) {
        //æ ¹-å­èŠ‚ç‚¹
        if (root == null) {
            return res;
        }
        res.add(root.val);
        for (Node node : root.children) {
            preorder(node);
        }
        return res;
    }

}
