class Solution {
    public int robotSim(int[] commands, int[][] obstacles) {
        //1ã€éšœç¢ç‰©ä½ç½®æè¿°ï¼š
        //ç¬¬iä¸ªéšœç¢ç‰©åæ ‡ï¼šx = obstacles[i][0], y = obstacles[i][1]
        //å¦‚ä½•æ£€æŸ¥å½“å‰ç‚¹æ˜¯ä¸æ˜¯éšœç¢ç‚¹ï¼Ÿè£…å…¥setå­—å…¸å®æ—¶æŸ¥è¯¢
        //2ã€æŒ‡ä»¤æè¿°ï¼š
        //2.1 è½¬å‘ï¼šå‘å·¦ã€å‘å³
        //åæ ‡å¹³é¢ä¸ŠåŒ—ä¸‹å—å·¦è¥¿å³ä¸œæ–¹å‘å¯¹åº”ï¼šdirection[0ï¼Œ1ï¼Œ2ï¼Œ3]
        //å³è½¬ä¸€æ¬¡åæ–¹å‘=ï¼ˆåŸæ–¹å‘+1ï¼‰% 4
        //å·¦è½¬ä¸€æ¬¡=å³è½¬ä¸‰æ¬¡æ–¹å‘=ï¼ˆåŸæ–¹å‘+3ï¼‰% 4
        //2.2 ä½ç§»ï¼šæ­¥é•¿1
        //ä½ç§»æ–¹å‘åªæœ‰ä¸ŠåŒ—ä¸‹å—å·¦è¥¿å³ä¸œå››ä¸ªï¼Œå¯¹åº”çš„xyåæ ‡å˜åŒ–move[[0,1],[1,0],[0,-1],[-1,0]]ï¼Œä¸€ç»´ä¸‹æ ‡å¯¹åº”æ–¹å‘
        //ä½ç§»å…¬å¼: x += move[ direction[i] ][0]
        //        y += move[ direction[i] ][1]

        //æ„å»ºä½ç§»æ•°ç»„, å¯¹åº”æ–¹å‘æ•°ç»„[0, 1, 2, 3]
        int[][] move = {{0, 1}, {1, 0}, {0, -1}, {-1, 0}};
        //æ„å»ºéšœç¢ç‰©åæ ‡å­—å…¸
        Set<Pair<Integer, Integer>> obstaclesSet = new HashSet<>();
        for (int[] obstacle : obstacles) {
            obstaclesSet.add(new Pair<>(obstacle[0], obstacle[1]));
        }
        int res = 0;//é¢˜æ±‚æœ€å¤§æ¬§å¼è·ç¦»

        //å¼€å§‹æ‰§è¡Œå‘½ä»¤ï¼Œåˆå§‹ä½ç½®ï¼ˆ0ï¼Œ0ï¼‰ï¼Œåˆå§‹æ–¹å‘åŒ—=0
        int currX = 0, currY = 0;
        int currDire = 0;
        for (int cmd : commands) {
            //å…ˆå¤„ç†è½¬å‘å‘½ä»¤:-1å³è½¬ï¼Œ-2å·¦è½¬
            if (cmd < 0) {
                currDire = cmd == -1 ? (currDire + 1) % 4 : (currDire + 3) % 4;
            }
            //å¤„ç†ä½ç§»å‘½ä»¤
            for (int i = 1; i <= cmd; i++) {
                int newX = currX + move[currDire][0];
                int newY = currY + move[currDire][1];
                //æ£€æŸ¥éšœç¢ç‚¹
                if (obstaclesSet.contains(new Pair<>(newX, newY))) {
                    break;//é‡åˆ°éšœç¢ç‚¹ï¼Œä¸­æ–­ä½ç§»
                } else {
                    currX = newX;
                    currY = newY;
                    res = Math.max(res, currX * currX + currY * currY);
                }
            }

        }
        return res;
    }
}
