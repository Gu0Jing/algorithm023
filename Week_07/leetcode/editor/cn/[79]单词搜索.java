class Solution {
    int m;
    int n;
    char[][] board;
    String word;
    int[][] direction = {{1,0},{-1,0},{0,1},{0,-1}};

    public boolean exist(char[][] board, String word) {
        m = board.length;
        n = board[0].length;
        this.board = board;
        this.word = word;
        for (int i = 0; i < m; i++) {
            for (int j = 0; j < n; j++) {
                if (board[i][j] == word.charAt(0)) {
                    if (dfs(i, j, 0)) {
                        return true;
                    }
                }
            }
        }
        return false;
    }

    private boolean dfs(int i, int j, int index) {
        if (i < 0 || i > m - 1 || j < 0 || j > n - 1 || index > word.length() - 1) {
            return false;
        }
        if (word.charAt(index) != board[i][j]) {
            return false;
        }
        if (index == word.length()-1) {
            return true;
        }
        char tmp = board[i][j];
        board[i][j] = ' ';
        for (int[] dir : direction) {
            int newX = i + dir[0];
            int newY = j + dir[1];
            if (dfs(newX, newY, index + 1)) {
                return true;
            }
        }
        board[i][j] = tmp;
        return false;
    }
}
